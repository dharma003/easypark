package com.org.easypark.vo;

public class ParkInfo {
	
	private String parkOwnerId;
	private String parkAvailability;
	private String address1;
	private String address2;
	private String city;
	private String country;
	private String parkOwnerMobileNo;
	
	public String getParkOwnerId() {
		return parkOwnerId;
	}
	public void setParkOwnerId(String parkOwnerId) {
		this.parkOwnerId = parkOwnerId;
	}
	public String getParkAvailability() {
		return parkAvailability;
	}
	public void setParkAvailability(String parkAvailability) {
		this.parkAvailability = parkAvailability;
	}
	public String getAddress1() {
		return address1;
	}
	public void setAddress1(String address1) {
		this.address1 = address1;
	}
	public String getAddress2() {
		return address2;
	}
	public void setAddress2(String address2) {
		this.address2 = address2;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getParkOwnerMobileNo() {
		return parkOwnerMobileNo;
	}
	public void setParkOwnerMobileNo(String parkOwnerMobileNo) {
		this.parkOwnerMobileNo = parkOwnerMobileNo;
	}
	
}
